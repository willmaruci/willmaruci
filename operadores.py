# Operações Matemáticas (Aritméticas)

"""
Soma: +
Subtração: -
Multiplicação: *
Divisão: /
Divisão inteira: //
Resto da divisão: %
Potência: **

"""

numero1 = 10
numero2 = 20

print (numero1 + numero2)
print (numero1 - numero2)
print (numero1 * numero2)
print (numero1 / numero2)
print (numero1 // numero2)
print (22 % 5)
print (4 ** 2)

# Operadores Booleanos

